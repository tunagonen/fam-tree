// Auto-generate familyConnections from familyTreeData
function generateFamilyConnections(tree: FamilyTree): FamilyConnection[] {
  const connections: FamilyConnection[] = [];
  const addedSpouses = new Set<string>();
  for (const memberId in tree.members) {
    const member = tree.members[memberId];
    // Spouse connection (avoid duplicates)
    if (member.spouseIds && member.spouseIds.length > 0) {
      for (const spouseId of member.spouseIds) {
        const key1 = `${memberId}-${spouseId}`;
        const key2 = `${spouseId}-${memberId}`;
        if (!addedSpouses.has(key1) && !addedSpouses.has(key2)) {
          connections.push({ from: memberId, to: spouseId, type: "spouse" });
          addedSpouses.add(key1);
        }
      }
    }
    // Bloodline connections
    if (member.children) {
      for (const childId of member.children) {
        connections.push({ from: memberId, to: childId, type: "bloodline" });
      }
    }
  }
  return connections;
}

